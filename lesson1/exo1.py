# Écrivez un programme qui demande son prénom à l'utilisateur, 
# puis qui lui dit bonjour en utilisant son prénom.

# Demander le prénom à l'utilisateur
prenom = input("Quel est votre prénom ? ")

# Dire bonjour avec le prénom
print(f"Bonjour {prenom} !")
print(f"Ravi de vous rencontrer, {prenom} !")